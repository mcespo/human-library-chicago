<article class="container p-10 prose lg:prose-xl">
    <h1>
        A Human Library is an event that aims to create dialogue and understanding between people.
    </h1>
    <p>
        “Books” are community members who have volunteered to share their experiences of prejudice
        and/or discrimination based on race, religion, sexual preference, class, gender identity,
        disability, or another aspect of their lives. “Readers” and “Books” engage in a dialogue
        designed to expose people to experiences that are different from their own and to encourage
        a deeper understanding among members of a community.
    </p>
    <p>Interested in volunteering with us? Apply to volunteer today!</p>
    <p>
        There are a lot of things the Chicago community can gain from this event. Participants, both
        volunteers and the public, can gain the understanding that dialogue between people can work
        to break the barriers that separate us. Additionally, participants will walk away with the
        mutual understanding of being human beyond a single identity. Our volunteers cherish the
        experience to share their narrative with others and forge new connections.
    </p>
    <p>
        The Human Library is an international movement that started in Denmark in the year 2000 and
        has since made its way through over 70 countries. Human Library Chicago was created with the
        permission of the Human Library Organization. The Human Library Chicago team is working hard
        to bring regularly scheduled Human Library events to the Chicago area. Please fill out our
        contact form if you are interested in volunteering. We are always looking to meet more
        people interested in the Human Library mission.
    </p>
    <p>
        Our organization is administered by Intersection, a not for profit with the purpose of
        engaging and educating communities through social contact and raise awareness of social
        issues.
    </p>
</article>
